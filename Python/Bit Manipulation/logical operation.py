a = 14
b = 4

#Logical AND Operation:
print("A & B", a&b)

#Logical OR Operation:
print("A | B", a|b)

#Logical NOT Operation:
print("~A", ~a)

#Logical XOR Operation:
print("A ^ B", a^b)

#Shifting Bits to Left:
print("Left Shift Of A", a << 1)

#Shifting Bits to Right:
print("Right Shift Of A", a >> 1)