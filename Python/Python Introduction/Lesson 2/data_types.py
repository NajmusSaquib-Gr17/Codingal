#Data Types in Python
def dataTypes():
    age = 15
    name = "Saquib"
    isStudent = True
    weight = 52.9

    print(f"Name: {name}")
    print(f"Date Type of {name} is {type(name)}")

    age = int(age)
    print(f"Age: {age}")
    print(f"Date Type of {age} is {type(age)}")

    isStudent = bool(False)
    print(f"Is He a Student: {isStudent}")

    weight = int(weight)
    print(f"Weight: {weight}")
    print(f"Date Type of {weight} is {type(weight)}")

dataTypes()

