SELECT * FROM DEPARTMENT;

SELECT DEPT_ID AS 'DEPARTMENT CODE', 
COUNT(*) AS 'NO_OF_EMPLOYEES',
SUM(SALARY) AS 'TOTAl SALARY'
FROM DEPARTMENT
GROUP BY DEPT_ID

SELECT DEPT_ID, SUM(SALARY)
FROM  DEPARTMENT
GROUP BY DEPT_ID

SELECT DEPT_ID, MAX(SALARY)
FROM  DEPARTMENT
GROUP BY DEPT_ID

SELECT MANAGER_ID AS 'MANAGER_ID',
SUM(SALARY) AS 'TOTAL SALARY'
FROM DEPARTMENT
WHERE MANAGER_ID = 'M004'
